/*Comandos em Git

Git clone <URL do Projeto> => para clonar um projeto

Git add => inclui seus arquivos no seu repositório local

Git commit -m => faz o commit local

Git push => sobe o seu código local para o servidor Git

Git fetch => busca as branchs no servidor (Branch é uma ramificação do seu código. Pode separar versões do código que que vai trabalhar agora, ou outra que vai usar depois)

Git status => verifica os arquivospendentes de sync com o servidor remoto, e visualiza a branch que esta trabalhando.

Git pull => pega o código que foi alterado no servidor, e atualiza no seu repositório local.

*/